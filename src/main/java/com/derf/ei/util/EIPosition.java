package com.derf.ei.util;

public class EIPosition {
	private int x;
	private int y;
	private int z;
	
	public EIPosition(int x, int y, int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getZ() {
		return z;
	}
}
